package src.loops;

public class Do_While_Loop_1 {
    public static void main(String[] args) {

        // Do While Loop

        //In While loop = Initialisation -> Condition -> Body -> Increment/Decrement
        //In Do While loop = Initialisation -> body -> Condition -> Increment/Decrement

        int i=1;                    // Initialisation
        do {
            System.out.println(i);  // Body
            i++;                    // Increment
        }while (i<=5);              // Condition


    }
}
