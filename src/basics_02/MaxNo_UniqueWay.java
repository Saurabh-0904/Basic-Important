package src.basics_02;

public class MaxNo_UniqueWay {
    public static void main(String[] args) {

        //Find a Maximum number
        // Max in two numbers we can find by below

        // using ? operator
        // If Else
        // Math function

        int a = 10;
        int b = 20;

        // Using Math function

        System.out.println(Math.max(a,b));

        // Also we can do
        System.out.println(Math.min(45,54));
        System.out.println(Math.pow(2,3)); // Power 2^3 = 8
        System.out.println(Math.max(45.24f,22.22f));

    }
}
